pub(super) const fn required_markers(smoke: SmokeKind) -> &'static [&'static str] {
    match smoke {
        SmokeKind::Boot => BOOT_REQUIRED_MARKERS,
        SmokeKind::Panic => PANIC_REQUIRED_MARKERS,
        SmokeKind::Exception => EXCEPTION_REQUIRED_MARKERS,
        SmokeKind::Timer => TIMER_REQUIRED_MARKERS,
    }
}

pub(super) const fn forbidden_markers(smoke: SmokeKind) -> &'static [&'static str] {
    match smoke {
        SmokeKind::Boot => BOOT_FORBIDDEN_MARKERS,
        SmokeKind::Panic | SmokeKind::Exception | SmokeKind::Timer => &[],
    }
}
